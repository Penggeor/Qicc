function crawler(company) {
  // const company = ['深圳集秀创意科技有限公司', '深圳市国盛建设工程有限公司']

  // 获取当前 key 参数并将其解密成对应的中文，比如 https://www.qcc.com/web/search?key=%E6%B7%B1%E5%9C%B3%E9%9B%86%E7%A7%80%E5%88%9B%E6%84%8F%E7%A7%91%E6%8A%80%E6%9C%89%E9%99%90%E5%85%AC%E5%8F%B8
  // 将 key 参数解密成 “深圳集秀创意科技有限公司”
  const key = decodeURI(location.href.split('=')[1])
  console.log(key)

  // 匹配当前公司的名称
  const currentCompanyIndex = company.findIndex((item) => {
    // 将英文括号替换成中文括号
    const itemWithChineseBracket = item.replace(/\(/g, '（').replace(/\)/g, '）')
    return itemWithChineseBracket === key
  })
  const currentCompany = company[currentCompanyIndex]
  console.log('当前公司的名称是：' + currentCompany)
  console.log('当前公司的索引是：' + currentCompanyIndex)
  console.log(currentCompanyIndex)
  // console.log(currentCompany)

  try {
    _fetchCurrentCompany(currentCompany, currentCompanyIndex)
  } finally {
    // 跳转到下一个公司
    // const nextCompany = company[company.indexOf(currentCompany) + 1]
    const nextCompany = company[currentCompanyIndex + 1]
    if (nextCompany) {
      // 随机跳转到下一个公司，延迟时间大概在 3s 之间
      setTimeout(() => {
        location.href = 'https://www.qcc.com/web/search?key=' + nextCompany
      },
      Math.random() * 3000
      )
      // location.href = 'https://www.qcc.com/web/search?key=' + nextCompany
    }
  }
}

